	<h1>Help</h1>
	
	<h2>Creating</h2>
	
	<p>To create a new commentable document it's easy. You can either paste the URL of the page into our <a href="<?php echo site_url('create'); ?>">create form</a>, or add our bookmarklet (at the bottom of the create page) to your bookmarks bar and enjoy one-click creation!</p>
	
