	<h2>Commenting</h2>
	
	<p>Feel The Vibe lets you comment on each paragraph individually, much like scribbling in the margins of a book or paper. This means you can see comments in their context, with no need to scroll backwards and forwards between a comments section and the document you're interested in.</p>
	
	<h3>How can I add a comment?</h3>
	
	<p>To comment on a paragraph just make sure you're signed in, then click the paragraph you want to comment on. You'll be shown any existing comments for that paragraph and given the option to write your own.</p>
	
	<h2>Vibes</h2>
	
	<h3>What is a vibe?</h3>
	
	<p>A vibe is a simple 'thumbs up' or 'thumbs down' against a specific paragraph which you can use to express your approval or disapproval of what's being said.</p>
	
	<h3>How can I leave a vibe?</h3>
	
	<p>Make sure you're signed in, then hover over a paragraph. You'll see the thumb up and thumb down icons appear; just click the one you want. You'll see a red or green line appear down the right hand side of the paragraph (depending on which thumb you chose) and the appropriate thumb icon will become permanently visible.</p>
	
	<h3>How do I see other people's vibes?</h3>
	
	<p>We'll automatically display vibes by highlighting paragraphs in the appropriate colour and by showing a 'vibe total' next to them. You can toggle the display of vibes on or off using the option in the commenting box.</p>
	
