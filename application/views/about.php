	<h2>How The Magic Happens</h2>
	
	<ol>
		<li>You tell us which web page you want us to make commentable.</li>
		<li>We grab the contents of the web page and run it through some algorithms to extract just the content, and strip out things like navigation.</li>
		<li>We store the page contents and display them through our own commenting interface.</li>
		<li>You can leave your thoughts on individual paragraphs, much like scribbling in the margin of a book or newspaper article.</li>
	</ol>
	
